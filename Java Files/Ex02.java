// Write a JAVA program to print all odd number between 1 to 100.

public class Ex02 
{
    public static void main(String[] args) 
    {
        my_method();
        
    }

    public static void my_method()
    {
        for(int i =1 ;i<=100 ;i=i+2)
        {
            System.out.println(i);
        }
    }
}
